(function () {
  var rotate, timeline;

  rotate = function () {
    return $('#all-container .stacked-card:first-child')
      .fadeOut(400, 'swing', function () {
        return $('#all-container .stacked-card:first-child')
          .appendTo('#all-container')
          .hide();
      })
      .fadeIn(400, 'swing');
  };

  $('#all-container:nth-child(1) .next').click(function () {
    return rotate();
  });
}.call(this));

